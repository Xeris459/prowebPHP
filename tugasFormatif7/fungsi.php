<?php
function penjumlahan($bil1, $bil2){
    $hasil = $bil1 + $bil2;

    return $hasil;
}

function pengurangan($bil1, $bil2){
    $hasil = $bil1 - $bil2;

    return $hasil;
}

function pembagian($bil1, $bil2){
    $hasil = $bil1 / $bil2;

    return $hasil;
}

function perkalian($bil1, $bil2){
    $hasil = $bil1 * $bil2;

    return $hasil;
}